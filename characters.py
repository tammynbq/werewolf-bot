"""有名有姓的『角色 NPC』登记表。

通用补位 NPC（见 npc.py 的 `_PERSONAS`）只有一句性格；这里登记的是带**完整人物
设定**的角色：他们会被优先选进补位席，拥有稳定、可辨识的说话风格与三观，让某些
AI 玩家像一个真正的"人"而不是随机性格。

要再加新角色：在 `CHARACTER_NPCS` 末尾追加一个 `Character` 即可。
- `name`  展示名（游戏中匿名只显示座位号，仅结算揭示身份时出现，如「🤖Theo」）。
- `persona` 用**第二人称**「你是…」书写，会原样喂进 LLM 的 system 提示，所以请写成
  「对这名 AI 玩家说话」的口吻，别用第一人称、别夹带与本游戏无关的设定。

注意（人称 / 措辞约定）：
- 全程用「你」称呼这名角色；称呼别的玩家一律用座位号（如「3 号」）。
- persona 只描述**气质、说话风格、打牌方式与三观**，不要写成长篇身世自传——
  背景只用来塑造语气，角色不会在牌桌上主动报家门。
- 默认牌桌上其他人都只是「这一局的玩家/对手」，不带入任何牌局之外的私人关系。
"""
