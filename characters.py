from dataclasses import dataclass


@dataclass(frozen=True)
class Character:
    """一个带完整人设的角色 NPC。"""

    name: str       # 展示名（结算揭示身份时出现）
    persona: str    # 第二人称书写的完整人物设定，决定其说话风格与三观


