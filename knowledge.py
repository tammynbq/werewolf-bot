_NO_SHERIFF_LINE = "警长/警徽流"


def playbook_for(roles_present, *, has_sheriff: bool = False) -> str:
    """按本局实际出现的角色拼出常识手册：讲清在场神职，提醒别提不存在的机制。"""
    present = set(roles_present)
    order = [Role.WEREWOLF, Role.WOLF_KING, Role.SEER, Role.WITCH,
             Role.HUNTER, Role.GUARD, Role.IDIOT, Role.KNIGHT, Role.VILLAGER]
    names = [r.cn for r in order if r in present]
    board_line = "· 本局板子有【" + " / ".join(names) + "】"
    if has_sheriff:
        board_line += " + 警长竞选"
    board_line += "。"

    absent = []
    for r, name in [(Role.HUNTER, "猎人"), (Role.GUARD, "守卫"),
                    (Role.IDIOT, "白痴"), (Role.KNIGHT, "骑士"),
                    (Role.WOLF_KING, "白狼王")]:
        if r not in present:
            absent.append(name)
    if not has_sheriff:
        absent.append("警长/警徽流")
    if absent:
        board_line += "没有" + "、".join(absent) + "——发言里别提这些不存在的角色或机制。"

    tips = [board_line]
    tips.append("· 狼人：夜里刀人、白天伪装好人，可自刀骗女巫解药。")
    for r in order:
        if r in present and r in _ROLE_TIPS:
            tips.append(_ROLE_TIPS[r])
    if Role.VILLAGER in present:
        tips.append("· 平民：无技能，靠逻辑和票型找狼。")
    if has_sheriff:
        tips.append(_SHERIFF_TIPS)

    return "\n".join([_PLAYBOOK_HEAD, *tips, _PLAYBOOK_TAIL])


